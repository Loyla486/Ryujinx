namespace Ryujinx.Audio
{
    /// <summary>
    /// The playback state of a track
    /// </summary>
    public enum PlaybackState
    {
        /// <summary>
        ///  The track is currently playing
        /// </summary>
        Playing = 0,
        /// <summary>
        /// The track is currently stopped
        /// </summary>
        Stopped = 1
    }
}